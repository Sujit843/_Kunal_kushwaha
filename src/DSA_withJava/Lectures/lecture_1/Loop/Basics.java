package DSA_withJava.Lectures.lecture_1.Loop;

public class Basics {
    public static void main(String[] args) {

        int count = 1;
        while (count != 5){
            System.out.println(count);
            count++;
        }
        // for loop
        for (int i = 1;  i != 5; i++){
            System.out.println(i);
        }


    }
}
